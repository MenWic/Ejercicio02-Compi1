    private void analizarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_analizarButtonActionPerformed
        Reader reader = new StringReader(TextArea.getText());
        automata = new Lexer(reader);
        int columna;
        int fila;

        try {
            int output = automata.yylex();
            while (output != Lexer.YYEOF) {
                automata.yylex();
            }
            TextAreaResults.append("Palabras con 1 vocal = " + automata.getUnaVocal() + "\n");
            TextAreaResults.append("Palabras con 2 vocales = " + automata.getDosVocales() + "\n");
            TextAreaResults.append("Palabras con 3 vocales = " + automata.getTresVocales() + "\n");
            TextAreaResults.append("Palabras con 4 vocales = " + automata.getCuatroVocales() + "\n");
            TextAreaResults.append("Palabras con 5 vocales = " + automata.getCincoVocales() + "\n");

            for (int i = 0; i < automata.getNumeros().size(); i++) {
                String texto = automata.getNumeros().get(i);
                TextAreaResults.append(texto + "\n");
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_analizarButtonActionPerformed

